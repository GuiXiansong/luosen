import java.io.Serializable;

/**
 * 物流信息
 */
public class Logistics{
    //编号
    private Long id;
    //出发地店铺id
    private Long startStoreId;
    //目的地店铺id
    private Long endStoreId;
    //商品id
    private Long goodsId;
    //商品数量（仅仅针对不是按重量算的商品）
    private Double count;
    //路程(默认为千米)
    private Double distance;
    //物品重量
    private Double weight;
    //运费
    private Double freight;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getStartStoreId() {
        return startStoreId;
    }

    public void setStartStoreId(Long startStoreId) {
        this.startStoreId = startStoreId;
    }

    public Long getEndStoreId() {
        return endStoreId;
    }

    public void setEndStoreId(Long endStoreId) {
        this.endStoreId = endStoreId;
    }

    public Long getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(Long goodsId) {
        this.goodsId = goodsId;
    }

    public Double getCount() {
        return count;
    }

    public void setCount(Double count) {
        this.count = count;
    }

    public Double getDistance() {
        return distance;
    }

    public void setDistance(Double distance) {
        this.distance = distance;
    }

    public Double getWeight() {
        return weight;
    }

    public void setWeight(Double weight) {
        this.weight = weight;
    }

    public Double getFreight() {
        return freight;
    }

    public void setFreight(Double freight) {
        this.freight = freight;
    }

    @Override
    public String toString() {
        return "Logistics{" +
                "id=" + id +
                ", startStoreId=" + startStoreId +
                ", endStoreId=" + endStoreId +
                ", goodsId=" + goodsId +
                ", count=" + count +
                ", distance=" + distance +
                ", weight=" + weight +
                ", freight=" + freight +
                '}';
    }
}
